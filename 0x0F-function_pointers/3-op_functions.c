#include "3-calc.h"

/**
 * op_add - addes two numbers
 *
 * @a: the first number, int
 * @b: the second number, int
 *
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 *
 * @a: the first number, int
 * @b: the second number, int
 *
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multply two numbers
 *
 * @a: the first number, int
 * @b: the second number, int
 *
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - devides two numbers
 *
 * @a: the first number, int
 * @b: the second number, int
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modelus operation on two numbers
 *
 * @a: the first number, int
 * @b: the second number, int
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
